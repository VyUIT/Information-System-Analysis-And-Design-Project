    public static void showNhanVien(){
        List<NHANVIEN> nhanvien = new ArrayList<>();
        NHANVIEN_BUS nv = new NHANVIEN_BUS();
        Component rootPane = null;
        if(nv.getListNhanVien() != null){
            javax.swing.table.DefaultTableModel mode;
            mode = (javax.swing.table.DefaultTableModel)jTable1.getModel();
            mode.setNumRows(0);
            nhanvien = nv.getListNhanVien();
            for(int i = 0; i < nhanvien.size(); i++){
                mode.addRow(new Object[]{nhanvien.get(i).getTENNV(), nhanvien.get(i).getNAMSINH(),
                    nhanvien.get(i).getGIOITINH(), nhanvien.get(i).getCMND()
                    , nhanvien.get(i).getDIACHI(), nhanvien.get(i).getSDT(),
                    nhanvien.get(i).getNGAYVAOLAM(),nhanvien.get(i).getTENQL()
                });
            }
        }
        else
            JOptionPane.showMessageDialog(rootPane,"Không có dữ liệu để hiển thị");
    }  
