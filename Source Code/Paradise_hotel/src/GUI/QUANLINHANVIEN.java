    private void jBXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBXoaActionPerformed
        // TODO add your handling code here:
        javax.swing.table.DefaultTableModel mode;
        mode = (javax.swing.table.DefaultTableModel)this.jTable1.getModel();
        NHANVIEN_BUS nhanvien = new NHANVIEN_BUS();
        Component rootPane = null;
        NHANVIEN nv = new NHANVIEN();
        if(nhanvien.findNhanVien(jtffind.getText()) == true){
            nv = nhanvien.getNhanVienByCMND(jtffind.getText());
            mode.setNumRows(0);
            mode.addRow(new Object[]{nv.getMANV(), nv.getTENNV(), nv.getNAMSINH(),
                nv.getGIOITINH(), nv.getCMND()
                , nv.getDIACHI(), nv.getSDT(),
                nv.getEMAIL(), nv.getNGAYVAOLAM(), nv.getTENTAIKHOAN(),
                nv.getMATKHAU(), nv.getNGUOIQUANLY(), nv.getTENLOAITK()
            });
            int buttonoption = JOptionPane.showConfirmDialog(rootPane,"Bạn có chắc chắn muốn xóa nhân viên");
            if(buttonoption == JOptionPane.YES_OPTION){
                boolean n = nhanvien.deleteNhanVien(jtffind.getText());
                mode.removeRow(0);
                if(n == true)
                JOptionPane.showMessageDialog(rootPane,"Xóa nhân viên thành công");
                else
                JOptionPane.showMessageDialog(rootPane,"Xóa nhân viên không thành công");
            }
        }
        else if(jTable1.getSelectedRow() != -1){
            int column = 3;
            int row = jTable1.getSelectedRow();
            String find = jTable1.getModel().getValueAt(row, column).toString();
            nv = nhanvien.getNhanVienByCMND(find);
            mode.setNumRows(0);
            mode.addRow(new Object[]{nv.getMANV(), nv.getTENNV(), nv.getNAMSINH(),
                nv.getGIOITINH(), nv.getCMND()
                , nv.getDIACHI(), nv.getSDT(),
                nv.getEMAIL(), nv.getNGAYVAOLAM(), nv.getTENTAIKHOAN(),
                nv.getMATKHAU(), nv.getNGUOIQUANLY(), nv.getTENLOAITK()
            });
            int buttonoption = JOptionPane.showConfirmDialog(rootPane,"Bạn có chắc chắn muốn xóa nhân viên");
            if(buttonoption == JOptionPane.YES_OPTION){
                boolean n = nhanvien.deleteNhanVien(find);
                mode.removeRow(0);
                if(n == true)
                JOptionPane.showMessageDialog(rootPane,"Xóa nhân viên thành công");
                else
                JOptionPane.showMessageDialog(rootPane,"Xóa nhân viên không thành công");
            }
        }
        else{
            int button = JOptionPane.showConfirmDialog(rootPane, "Không tìm thấy nhân viên. Vui lòng nhập lại");
            if(button == JOptionPane.NO_OPTION)
            System.exit(0);
        }

    }//GEN-LAST:event_jBXoaActionPerformed

