    public static void ShowBangKeMONAN()
    {
        List<BANGKE_MONAN_DTO> bks = new ArrayList<BANGKE_MONAN_DTO>();
        BANGKE_MONAN_BUS bk = new BANGKE_MONAN_BUS();
        bks = bk.getListBKMonAnbyMAHD(Integer.parseInt(jMAHD.getText()));
        Component frame = null;
        
        if(bks!= null)
        {
            DefaultTableModel model = new DefaultTableModel ();
            model = (DefaultTableModel) jtMA.getModel();
            model.setNumRows(0);
            
            for (int i=0; i < bks.size(); i++)
                model.addRow(new Object[]{bks.get(i).getMAMON(),bks.get(i).getMAHD(),bks.get(i).getNGAY(),bks.get(i).getTHANHTIEN()});
        }
        else
            JOptionPane.showMessageDialog(frame, "Không có dữ liệu để hiển thị");
    }
    
    public void ShowInfo()
    {
        List <MONAN> mas = new ArrayList <MONAN> ();
        MONAN_BUS mab = new MONAN_BUS();
        mas = mab.getListMonAn();
        for(int i=0; i < mas.size(); i++)
            jcbMA.addItem(mas.get(i).getTen());
    }
    
    public void gia (String value)
    {
        
        MONAN_BUS mab = new MONAN_BUS();
        MONAN ma = new MONAN();
        ma = mab.getMonAnByName(value);
        String gia = Integer.toString(ma.getGia());
        jGia.setText(gia);
        
    }
    
    private void jInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jInsertActionPerformed
        // TODO add your handling code here:
        
        BANGKE_MONAN_BUS bks = new BANGKE_MONAN_BUS();
        BANGKE_MONAN_DTO bk = new BANGKE_MONAN_DTO();
        MONAN_BUS ma = new MONAN_BUS();
        MONAN temp = new MONAN();
        SimpleDateFormat df = new SimpleDateFormat("dd-MMM-yyyy");
        String date = df.format(jDateChooser.getDate());
        String ten = (String)jcbMA.getSelectedItem();
        temp = ma.getMonAnByName(ten);
        int sl = Integer.parseInt(jSOLUONG.getValue().toString());
        int gia =temp.getGia() * sl;
        jTHANHTIEN.setText(Integer.toString(gia));
        
        bk.setMAHD(Integer.parseInt(jMAHD.getText()));
        bk.setMAMON(temp.getMamon());
        bk.setNGAY(date);
        bk.setSOLUONG(sl);
        bk.setTHANHTIEN(gia);
        
        
        
        Component frame = null;
        int buttonoption = JOptionPane.showConfirmDialog(frame,"Xác nhận đặt món ăn ?");
        if(buttonoption == JOptionPane.YES_OPTION)
        {
            if(bks.INSERT_BANGKE_MONAN(bk))
            {
                ShowBangKeMONAN();
                JOptionPane.showMessageDialog(frame, "Đặt món ăn thành công");
            }
            else
                JOptionPane.showMessageDialog(frame, "Đặt món ăn thất bại");
        }
        else
            JOptionPane.showMessageDialog(frame, "Đặt món ăn thất bại");
    }//GEN-LAST:event_jInsertActionPerformed

