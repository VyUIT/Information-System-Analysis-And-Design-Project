    public void ShowBangKeMONAN ()
    {
        List<BANGKE_MONAN_DTO> bks = new ArrayList<BANGKE_MONAN_DTO>();
        BANGKE_MONAN_BUS bk = new BANGKE_MONAN_BUS();
        Component frame = null;
        
        int Ma_HD = Integer.parseInt(jMAHD.getText());
        
        if(bk.getListBKMonAnbyMAHD(Ma_HD) != null)
        {
            javax.swing.table.DefaultTableModel mode;
            mode = (javax.swing.table.DefaultTableModel) this.jBKMA.getModel();
            mode.setNumRows(0);
            bks = bk.getListBKMonAnbyMAHD(Ma_HD);
            for (int i=0; i < bks.size(); i++)
               
                mode.addRow(new Object[]{bks.get(i).getMAMON(),bks.get(i).getMAHD(),bks.get(i).getNGAY(),bks.get(i).getSOLUONG(),bks.get(i).getTHANHTIEN()});
        }
        else
            JOptionPane.showMessageDialog(frame, "Không có dữ liệu để hiển thị");
    }
