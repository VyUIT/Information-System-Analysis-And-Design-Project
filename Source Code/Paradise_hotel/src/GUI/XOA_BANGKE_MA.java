    private void jDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDeleteActionPerformed
        // TODO add your handling code here:
        
        BANGKE_MONAN_DTO bk = new BANGKE_MONAN_DTO();
        bk.setMAMON(Integer.parseInt(jMAMON.getText()));
        bk.setMAHD(Integer.parseInt(jMAHD.getText()));
        bk.setNGAY(jNgay.getText());
        bk.setSOLUONG(Integer.parseInt(jSoLuong.getText()));
        bk.setTHANHTIEN(Integer.parseInt(jTHANHTIEN.getText()));

        Component frame = null;

        if(jMAMON.getText() != null)
        {
            BANGKE_MONAN_BUS bks = new BANGKE_MONAN_BUS();

            int buttonoption = JOptionPane.showConfirmDialog(frame,"Xác nhận xóa bảng kê món ăn ?");
            if(buttonoption == JOptionPane.YES_OPTION)
            {
                boolean rs = bks.DELETE_BANGKE_MONAN(bk);
              
                if(rs==true)
                {
                    DefaultTableModel model = (DefaultTableModel)jBKMA.getModel();
                    model.removeRow(row);
                    JOptionPane.showMessageDialog(frame, "Xóa bảng kê món ăn thành công");
                }
                else
                JOptionPane.showMessageDialog(frame, "Xóa bảng kê món ăn thất bại");
            }
            else
            {
                int button = JOptionPane.showConfirmDialog(frame, "Không tìm thấy bảng kê món ăn");
                if(button == JOptionPane.NO_OPTION)
                System.exit(0);
            }
        }
        else
        JOptionPane.showMessageDialog(frame,"Vui lòng click chọn bảng kê món ăn muốn xóa!");
    }//GEN-LAST:event_jDeleteActionPerformed

