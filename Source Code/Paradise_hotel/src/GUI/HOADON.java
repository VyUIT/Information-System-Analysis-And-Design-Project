    private void jPayButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jPayButtonActionPerformed
        // TODO add your handling code here:
        Component frame = null;
        int buttonoption = JOptionPane.showConfirmDialog(frame,"Xác nhận thanh toán hóa đơn ?");
        HOADONTHANHTOAN_BUS hdb = new HOADONTHANHTOAN_BUS();
        HOADONTHANHTOAN_DTO hd = new HOADONTHANHTOAN_DTO();
        if(buttonoption == JOptionPane.YES_OPTION)
        {
     
            if(hdb.Check_thanh_toan(Integer.parseInt(jMaHD.getText())) == true)
            {
                this.jTINHTRANG.setSelected(true);
                int tongtien = hdb.TONG_TIEN(Integer.parseInt(jMaHD.getText()));
                jTONGTIEN.setText(Integer.toString(tongtien));
                JOptionPane.showMessageDialog(frame, "Thanh toán hóa đơn thanh toán thành công");
            }
            else
                JOptionPane.showMessageDialog(frame, "Thanh toán hóa đơn thanh toán thất bại");
            
        }
        else
            JOptionPane.showMessageDialog(frame, "Thanh toán hóa đơn thanh toán thất bại");
    }//GEN-LAST:event_jPayButtonActionPerformed

