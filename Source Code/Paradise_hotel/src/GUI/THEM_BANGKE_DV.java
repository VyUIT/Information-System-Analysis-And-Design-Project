     public static void ShowBangKeDV()
    {

        List<BANGKE_DV_DTO> bks = new ArrayList<BANGKE_DV_DTO>();
        BANGKE_DV_BUS bk = new BANGKE_DV_BUS();
        Component frame = null;
        
        
        if(jMAHD.getText().equals("") == true)
        {
            JOptionPane.showMessageDialog(frame,"Vui lòng nhập mã HD");
        }
        else        
        {
            bks = bk.getListBangKeDVbyMAHD(Integer.parseInt(jMAHD.getText()));

            if(bks != null)
            {
                DefaultTableModel model = new DefaultTableModel ();
                model = (DefaultTableModel) jtBKDV.getModel();
                model.setNumRows(0);
                for (int i=0; i < bks.size(); i++)
                model.addRow(new Object[]{bks.get(i).getMADV(),bks.get(i).getMAHD(),bks.get(i).getNGAY(),bks.get(i).getTHANHTIEN()});
            }
            else
                JOptionPane.showMessageDialog(frame, "Không có dữ liệu để hiển thị");
        }
    }
    
    
    public void ShowInfo()
    {
        BUS_DICHVU dv = new BUS_DICHVU();
        List<DICHVU> dvs = new ArrayList <DICHVU> ();
        dvs = dv.BUS_LoadData();
        for (int i = 0; i < dvs.size(); i++)
            jcbDV.addItem(dvs.get(i).getTENDV());
    }
    public void THANHTIEN(String value)
    {
        BUS_DICHVU bdv = new BUS_DICHVU();
        DICHVU dv = new DICHVU();
        dv =bdv.BUS_getDVbyten(value);
        String gia = Integer.toString(dv.getGIADV());
        jTHANHTIEN.setText(gia);
    }
    
    
    

    
