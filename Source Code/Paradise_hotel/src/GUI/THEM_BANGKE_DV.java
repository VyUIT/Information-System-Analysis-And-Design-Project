    private void jInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jInsertActionPerformed
        // TODO add your handling code here:
        BANGKE_DV_BUS bks = new BANGKE_DV_BUS();
        BANGKE_DV_DTO bk = new BANGKE_DV_DTO();
        BUS_DICHVU dv = new BUS_DICHVU();
        
        SimpleDateFormat std = new SimpleDateFormat("dd-MM-YY");
        String date = std.format(Ngay.getDate());
        
        DICHVU temp = new DICHVU();        
        String ten = jcbDV.getSelectedItem().toString();
        temp.setMADV(dv.BUS_getDVbyten(ten).getMADV());
        
        
        bk.setMAHD(Integer.parseInt(jMAHD.getText()));
        bk.setMADV(temp.getMADV());
        bk.setNGAY(date);
        bk.setTHANHTIEN(Integer.parseInt(jTHANHTIEN.getText()));
        
        Component frame = null;
        
        int buttonoption = JOptionPane.showConfirmDialog(frame,"Xác nhận đặt dịch vụ ?");
        
        if(buttonoption == JOptionPane.YES_OPTION)
        {
            if(bks.INSERT_BANGKE_DV(bk)==true)
            {
                ShowBangKeDV();
                JOptionPane.showMessageDialog(frame, "Đặt dịch vụ thành công");
            }
            else
            {
                JOptionPane.showMessageDialog(frame, "Đặt dịch vụ thất bại");
            }
        }
        else
            JOptionPane.showMessageDialog(frame, "Đặt dịch vụ thất bại");
        
        
    }//GEN-LAST:event_jInsertActionPerformed

