public static void showMonAn(){
        List<MONAN> monans = new ArrayList<>();
        MONAN_BUS monan = new MONAN_BUS();
        Component rootPane = null; 
        if(monan.getListMonAn() != null){
            javax.swing.table.DefaultTableModel mode;
            mode = (javax.swing.table.DefaultTableModel)jtMA.getModel();
            mode.setNumRows(0);
            monans = monan.getListMonAn();
            for(int i = 0; i < monans.size();i++){
                mode.addRow(new Object[]{monans.get(i).getMamon(),monans.get(i).getTen(),monans.get(i).getGia(), monans.get(i).getTinhtrang()});
            }
        }
        else
            JOptionPane.showMessageDialog(rootPane, "Không có dữ liệu để hiển thị");
}
            

