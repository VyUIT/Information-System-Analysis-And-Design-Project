    private void jbFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbFindActionPerformed
        // TODO add your handling code here:
        MONAN_BUS monans = new MONAN_BUS();
        Component rootPane = null;
        MONAN monan = new MONAN();
        javax.swing.table.DefaultTableModel mode;
        mode = (javax.swing.table.DefaultTableModel)this.jtMA.getModel();
        if(monans.findMonAn(jtfFind.getText())){
            monan = monans.getMonAnByName(jtfFind.getText());
            mode.setNumRows(0);
            mode.addRow(new Object[]{monan.getMamon(),monan.getTen(),monan.getGia(), monan.getTinhtrang()});
        }
        else if(jTable1.getSelectedRow() != -1){
            int column = 1;
            int row = jTable1.getSelectedRow();
            String find = jTable1.getModel().getValueAt(row, column).toString();
            monan = monans.getMonAnByName(find);
            mode.setNumRows(0);
            mode.addRow(new Object[]{monan.getMamon(),monan.getTen(),monan.getGia(), monan.getTinhtrang()});
        }
        else{
            JOptionPane.showMessageDialog(rootPane,"Không tìm thấy món ăn. Vui lòng nhập lại" );
        }             
    }//GEN-LAST:event_jbFindActionPerformed

