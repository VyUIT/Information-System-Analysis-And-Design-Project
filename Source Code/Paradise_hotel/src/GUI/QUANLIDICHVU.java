    public static void ShowData ()
    {
        List <DICHVU> dvs = new ArrayList ();
        BUS_DICHVU dv = new BUS_DICHVU ();
        Component rootPane = null;
        dvs = dv.BUS_LoadData();
        if (dvs != null)
        {
            DefaultTableModel model = new DefaultTableModel ();
            model = (DefaultTableModel) jtQLDV.getModel();
            model.setRowCount(0);
            for (int i =0; i<dvs.size(); i++)
            {
                model.addRow(new Object [] {dvs.get(i).getMADV(), dvs.get(i).getTENDV(), dvs.get(i).getGIADV(), dvs.get(i).getTINHTRANG()});
            }
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane, "Không có dữ liệu để hiện");
        }
    }


