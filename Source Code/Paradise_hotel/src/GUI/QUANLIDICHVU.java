    private void jBXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBXoaActionPerformed
        // TODO add your handling code here:
        BUS_DICHVU bdv = new BUS_DICHVU ();
        DICHVU dv = new DICHVU ();

        if(jtQLDV.getSelectedRow() != -1){
            int index = jtQLDV.getSelectedRow();
            
            
            int buttonoption = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắc muốn xóa?");
            if(buttonoption == JOptionPane.YES_OPTION){
                
                DefaultTableModel model = new DefaultTableModel ();
                model = (DefaultTableModel) jtQLDV.getModel();
                int madv = (int) model.getValueAt(index, 0);
                String tendv = model.getValueAt(index, 1).toString();
                int giadv = (int) model.getValueAt(index, 2);
                String tinhtrang = model.getValueAt(index,3).toString();
                dv.setMADV(madv);
                dv.setTENDV(tendv);
                dv.setGIADV(giadv);
                dv.setTINHTRANG(tinhtrang);
                if(bdv.BUS_DELETEDICHVU(dv) == true)
                {
                    model.removeRow(index);
                    JOptionPane.showMessageDialog(null,"Xóa dịch vụ thành công");
                }
                else 
                    JOptionPane.showMessageDialog(null,"Xóa dịch vụ thất bại");
            }            
        }
        else{
            int button = JOptionPane.showConfirmDialog(null, "Không tìm thấy dịch vụ. Vui lòng nhập lại");
            if(button == JOptionPane.NO_OPTION)
               System.exit(0);
        }     
        
    }//GEN-LAST:event_jBXoaActionPerformed

    private void jBSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSuaActionPerformed
        // TODO add your handling code here:
        String madv = null,tendv = null,giadv = null,tinhtrang = null; 
        BUS_DICHVU bdv = new BUS_DICHVU();
        DICHVU dv = new DICHVU ();
        UPDATEDICHVU udv = new UPDATEDICHVU ();
        dv = bdv.BUS_getDVbyten(jtfFind.getText());
        if (dv == null) {
            madv = String.valueOf(dv.getMADV());
            tendv = dv.getTENDV();
            giadv = String.valueOf(dv.getGIADV());
            tinhtrang = dv.getTINHTRANG();
            JOptionPane.showMessageDialog(null, "Dữ liệu bị lỗi");
        }
        else { 
            // Hiển thị dữ liệu lên jFrame UPDATEDICHVU
            try{
            TableModel model = jtQLDV.getModel();
            int index = jtQLDV.getSelectedRow();

            //Tạo biến chuỗi để lưu thông tin lấy từ hàng được chọn
             madv = model.getValueAt(index, 0).toString();
             tendv = model.getValueAt(index, 1).toString();
             giadv = model.getValueAt(index, 2).toString();
             tinhtrang = model.getValueAt(index,3).toString();
             udv.setVisible(true);
            } catch (Exception e)
            {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn dịch vụ");
                udv.setVisible(false);
                
            }
        }
            
            //Điền các chzuỗi đã lưu vào các jtextfiel của UPDATEDICHVU
            udv.jtfMa.setText(madv);
            udv.jtfTen.setText(tendv);
            udv.jtfGia.setText(giadv);
            udv.jtfTinhtrang.setText(tinhtrang);
 
    }//GEN-LAST:event_jBSuaActionPerformed

