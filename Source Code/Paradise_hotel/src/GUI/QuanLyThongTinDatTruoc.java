    private void jBXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBXoaActionPerformed
        // TODO add your handling code here:
        PHIEUDATTRUOC_BUS pdt = new PHIEUDATTRUOC_BUS();
        int button = JOptionPane.showConfirmDialog(null,"Bạn có chắc chắn xóa phiếu đặt trước?");
        if(button == JOptionPane.YES_OPTION){
            if(jTable1.getRowCount() > 1){
                if(jTable1.getSelectedRow() != -1){
                    int find = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
                    boolean rs = pdt.deletePhieuDatTruoc(find);
                    if(rs == true){
                        JOptionPane.showMessageDialog(null,"Xóa phiếu đặt trước thành công");
                        showListPhieuDatTruoc();
                    }else{
                        JOptionPane.showMessageDialog(null,"Xóa phiếu đặt trước thất bại");
                        showListPhieuDatTruoc();
                    }
                }
                else
                    JOptionPane.showMessageDialog(null, "Vui lòng chọn phiếu đặt trước cần xóa");
            }else{
                boolean rs = pdt.deletePhieuDatTruoc(Integer.parseInt(jTable1.getValueAt(0, 0).toString()));
                if(rs == true){
                    JOptionPane.showMessageDialog(null,"Xóa phiếu đặt trước thành công");
                    showListPhieuDatTruoc();
                }else{
                    JOptionPane.showMessageDialog(null,"Xóa phiếu đặt trước thất bại");
                    showListPhieuDatTruoc();
                }
            }  
        }
    }//GEN-LAST:event_jBXoaActionPerformed

