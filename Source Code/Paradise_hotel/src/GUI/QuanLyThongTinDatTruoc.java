    private void jbFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbFindActionPerformed
        // TODO add your handling code here:
        PHIEUDATTRUOC_BUS phieudat = new PHIEUDATTRUOC_BUS();
        List<PHIEUDATTRUOC> pdt = new ArrayList<>();
        if(phieudat.findPhieuDatTruoc(jtffind.getText())){
            pdt = phieudat.getPhieuDatTruoc(jtffind.getText());
            DefaultTableModel mode = new DefaultTableModel();
            mode = (DefaultTableModel)jTable1.getModel();
            mode.setNumRows(0);
            for(int i = 0; i < pdt.size(); i++){
                mode.addRow(new Object[]{pdt.get(i).getMAPHIEU(), pdt.get(i).getMAPHONG(),
                    pdt.get(i).getTENKH(), pdt.get(i).getTENNV(), pdt.get(i).getNGAYDEN()
                    ,pdt.get(i).getNGAYROI(),pdt.get(i).getTINHTRANG(), pdt.get(i).getSONGUOIDIKEM()});
            }
        }else if(jTable1.getSelectedRow() != -1){
            int row = jTable1.getSelectedRow();
            PHIEUDATTRUOC pdt1 = new PHIEUDATTRUOC();
            pdt1.setMAPHIEU(Integer.parseInt(jTable1.getValueAt(row, 0).toString()));
            pdt1.setTENKH(jTable1.getValueAt(row, 2).toString());
            pdt1.setMAPHONG(Integer.parseInt(jTable1.getValueAt(row,1).toString()));
            pdt1.setTENNV(jTable1.getValueAt(row, 3).toString());
            pdt1.setNGAYDEN(jTable1.getValueAt(row, 4).toString());
            pdt1.setNGAYROI(jTable1.getValueAt(row, 5).toString());
            pdt1.setTINHTRANG(jTable1.getValueAt(row, 6).toString());
            pdt1.setSONGUOIDIKEM(Integer.parseInt(jTable1.getValueAt(row, 7).toString()));
            DefaultTableModel mode = new DefaultTableModel();
            mode = (DefaultTableModel)jTable1.getModel();
            mode.setNumRows(0);
            mode.addRow(new Object[]{pdt1.getMAPHIEU(), pdt1.getMAPHONG(),
                    pdt1.getTENKH(), pdt1.getTENNV(), pdt1.getNGAYDEN()
                    ,pdt1.getNGAYROI(), null});    
        }
        else
            JOptionPane.showMessageDialog(null,"Không tìm thấy phiếu đặt trước");
    }//GEN-LAST:event_jbFindActionPerformed

