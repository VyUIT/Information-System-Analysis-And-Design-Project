    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        // TODO add your handling code here:
        Component frame = null;
        if(juser.getText().equals("") == true && jpass.getText().equals("") == true)
            JOptionPane.showMessageDialog(frame, "Vui lòng nhập đầy đủ tên đăng nhập và mật khẩu");
        else if(juser.getText().equals("") == true)
            JOptionPane.showMessageDialog(frame, "Vui lòng nhập tên đăng nhập");
        else if(jpass.getText().equals("") == true)
            JOptionPane.showMessageDialog(frame, "Vui lòng nhập mật khẩu");
        else 
        {
            DAO_DANGNHAP dn = new DAO_DANGNHAP();
            NHANVIEN nv = new NHANVIEN ();
            

                if (dn.taikhoan(juser.getText(), jpass.getText()) != null)
                {
                    nv = dn.taikhoan(juser.getText(), jpass.getText());
                    if (nv.getMALOAITK() == 1)
                    {
                    Receptionists re = new Receptionists ();
                    re.setVisible(true);  
                    }
                    else
                    {
                        Manager ma = new Manager();
                        ma.setVisible(true);
                    }
                }
                
                else
                {
                    JOptionPane.showMessageDialog(null, "Đăng nhập thất bại");
                }
               
            
            
        }    
    }//GEN-LAST:event_LoginActionPerformed

