    private void jbthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbthemActionPerformed
        // TODO add your handling code here:
        PHIEUNHANPHONG_BUS pnp = new PHIEUNHANPHONG_BUS();
        PHIEUNHANPHONG np = new PHIEUNHANPHONG();
        np.setMAKH(Integer.parseInt(jtfmakh.getText()));
        NHANVIEN_TT nv = (NHANVIEN_TT)jmanv.getSelectedItem();
        np.setMANV(nv.getMANV());
        np.setMAPHONG(Integer.parseInt(jphong.getSelectedItem().toString()));
        Date date = jngayden.getDate();
        SimpleDateFormat dateformat = new SimpleDateFormat("dd-MM-YYYY");
        String ngayden = dateformat.format(date);
        np.setNGAYDEN(ngayden); 
        date = jngayroi.getDate();
        String ngayroi = dateformat.format(date);
        np.setNGAYROI(ngayroi);
        np.setSONGUOIDIKEM(Integer.parseInt(jsonguoidikem.getSelectedItem().toString()));
        boolean rs = pnp.addPhieuNhanPhong(np);
        if(rs == true){
            JOptionPane.showMessageDialog(null, "Thêm phiếu nhận phòng thành công");    
            showListPhieuNhanPhong();
        }else{
            JOptionPane.showMessageDialog(null, "Thêm phiếu nhận phòng thất bại");
        }

    }//GEN-LAST:event_jbthemActionPerformed

