    private void jbthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbthemActionPerformed
        // TODO add your handling code here:
        CSVC_BUS csvc = new CSVC_BUS();
        CSVC cs = new CSVC();
        cs.setTENCSVC(jtften.getText());
        cs.setGIACSVC(Integer.parseInt(jtfgia.getText()));
        boolean rs = csvc.addCSVC(cs);
        if(rs == true){
            JOptionPane.showMessageDialog(rootPane,"Thêm cơ sở vật chất thành công");
            showCSVC();
        }
        else
            JOptionPane.showMessageDialog(rootPane,"Thêm cơ sở vật chất thất bại");
    }//GEN-LAST:event_jbthemActionPerformed

