    private void jBSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSuaActionPerformed
        // TODO add your handling code here:
        PHONG p = new PHONG ();
        BUS_PHONG  bp = new BUS_PHONG ();


        if (jtfmap.getText() == "" && jtfttrp.getText() == "" && jtfmalp.getText() == "")
        {
            JOptionPane.showMessageDialog(rootPane, "Thông tin còn rỗng, không thể cập nhật");
        }
        else if (jtfmap.getText() == "" || jtfttrp.getText() == "" || jtfmalp.getText() == "")
        {
            JOptionPane.showMessageDialog(rootPane, "Thông tin còn rỗng, không thể cập nhật");
        }       
        else
        {
            p.setMaphong(Integer.parseInt(jtfmap.getText()));
            p.setTinhtrang(jtfttrp.getText());
            p.setLoaiphong(Integer.parseInt(jtfmalp.getText()));
            
            if (bp.BUS_UpdatePhong(p) == true)
            {
                QUANLIPHONG.ShowData();
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công");
                this.dispose();
            }
            else 
            {
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thất bại");
                this.dispose();
            }
        }
        this.dispose();
            
        
    }//GEN-LAST:event_jBSuaActionPerformed

