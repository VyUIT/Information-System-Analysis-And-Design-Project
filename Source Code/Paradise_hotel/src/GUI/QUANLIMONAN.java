    private void jBXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBXoaActionPerformed
        // TODO add your handling code here:
        javax.swing.table.DefaultTableModel mode;
        mode = (javax.swing.table.DefaultTableModel)this.jTable1.getModel();
        MONAN_BUS monan = new MONAN_BUS();
        Component rootPane = null;
        MONAN ma = new MONAN();
        if(monan.findMonAn(jtfFind.getText()) == true){
            ma = monan.getMonAnByName(jtfFind.getText());
            mode.setNumRows(0);
            mode.addRow(new Object[]{ma.getMamon(),ma.getTen(),ma.getGia(), ma.getTinhtrang()});
            int buttonoption = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc chắc muốn xóa?");
            if(buttonoption == JOptionPane.YES_OPTION){
                boolean rs = monan.deleteMonAn(jtfFind.getText());
                mode.removeRow(0);
                if(rs == true)
                JOptionPane.showMessageDialog(rootPane,"Xóa món ăn thành công");
                else
                JOptionPane.showMessageDialog(rootPane,"Xóa món ăn thất bại");
            }
        }
        else if(jTable1.getSelectedRow() != -1){
            int column = 1;
            int row = jTable1.getSelectedRow();
            String find = jTable1.getModel().getValueAt(row, column).toString();
            ma = monan.getMonAnByName(find);
            mode.setNumRows(0);
            mode.addRow(new Object[]{ma.getMamon(),ma.getTen(),ma.getGia(), ma.getTinhtrang()});
            int buttonoption = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc chắc muốn xóa?");
            if(buttonoption == JOptionPane.YES_OPTION){
                boolean rs = monan.deleteMonAn(find);
                mode.removeRow(0);
                if(rs == true)
                JOptionPane.showMessageDialog(rootPane,"Xóa món ăn thành công");
                else
                JOptionPane.showMessageDialog(rootPane,"Xóa món ăn thất bại");
            }
        }
        else{
            int button = JOptionPane.showConfirmDialog(rootPane, "Không tìm thấy món ăn. Vui lòng nhập lại");
            if(button == JOptionPane.NO_OPTION)
            System.exit(0);
        }
    }//GEN-LAST:event_jBXoaActionPerformed

