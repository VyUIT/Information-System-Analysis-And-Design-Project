public void FilterPhong (String ND, String NR) {
        List <PHONG> lp = new ArrayList <PHONG> ();
        BUS_PHONG bp = new BUS_PHONG ();
        Component rootpane = null;
        lp = bp.FilterPhong(ND,NR);
        if (lp != null)
        {
            DefaultTableModel model = new DefaultTableModel();
            model = (DefaultTableModel) jtQLP.getModel();
            model.setRowCount(0);
            for(int i =0; i<lp.size(); i++)
            {
                model.addRow(new Object [] {lp.get(i).getMaphong(), lp.get(i).getTinhtrang(), lp.get(i).getLoaiphong()});
            }
        }
        else
        {
            JOptionPane.showMessageDialog(rootpane, "Không có dữ liệu hiển thị");
        }
}
