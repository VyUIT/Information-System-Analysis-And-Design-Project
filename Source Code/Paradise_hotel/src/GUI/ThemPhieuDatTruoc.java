    private void jbthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbthemActionPerformed
        // TODO add your handling code here:
            PHIEUDATTRUOC_BUS pdt = new PHIEUDATTRUOC_BUS();
            PHIEUDATTRUOC dt = new PHIEUDATTRUOC();
            dt.setMAKH(Integer.parseInt(jtfmakh.getText()));
            NHANVIEN_TT nv = (NHANVIEN_TT)jmanv.getSelectedItem();
            dt.setMANV(nv.getMANV());
            dt.setMAPHONG(Integer.parseInt(jphong.getSelectedItem().toString()));
            Date date = jngayden.getDate();
            SimpleDateFormat dateformat = new SimpleDateFormat("dd-MM-YYYY");
            String ngayden = dateformat.format(date);
            dt.setNGAYDEN(ngayden); 
            date = jngayroi.getDate();
            String ngayroi = dateformat.format(date);
            dt.setNGAYROI(ngayroi);
            dt.setSONGUOIDIKEM(Integer.parseInt(jsonguoidikem.getSelectedItem().toString()));
            boolean rs = pdt.addPhieuDatTruoc(dt);
            if(rs == true)
                JOptionPane.showMessageDialog(null, "Thêm phiếu đặt trước thành công");
            else
                JOptionPane.showMessageDialog(null, "Thêm phiếu đặt trước thất bại");
    }//GEN-LAST:event_jbthemActionPerformed

