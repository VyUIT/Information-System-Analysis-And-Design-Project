    public void showdataforjphong(){
        PHIEUDATTRUOC_BUS pdt = new PHIEUDATTRUOC_BUS();
        List<Integer> maphong = new ArrayList<>();
        Date date = jngayden.getDate();
        SimpleDateFormat dateformat = new SimpleDateFormat("dd-MM-YYYY");
        String ngayden = dateformat.format(date);
        date = jngayroi.getDate();
        String ngayroi = dateformat.format(date);
        System.out.println(ngayden + " " + ngayroi);
        LOAIPHONG a = (LOAIPHONG)jloaiphong.getSelectedItem();
        maphong = pdt.getListPhongTrong(ngayden, ngayroi,a.getMALOAIPHONG());
        jphong.removeAllItems();
        for(int i = 0; i < maphong.size(); i++){
            this.jphong.addItem(String.valueOf(maphong.get(i)));
        }
        Random rd = new Random();
        int n = maphong.get(rd.nextInt(maphong.size()));
        System.out.println(n);
        jphong.setSelectedItem(String.valueOf(n));
    }
    
    public void showdataforjsonguoidikem(){
        LOAIPHONG lp = (LOAIPHONG)jloaiphong.getSelectedItem();
        int n = lp.getSOLUONGNGUOI();
        jsonguoidikem.removeAllItems();
        for(int i = 1; i < n; i++){
            jsonguoidikem.addItem(String.valueOf(i));
        }
    }
